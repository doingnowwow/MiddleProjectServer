package kr.or.ddit.vo;

import java.io.Serializable;

public class BookingInfoVO implements Serializable {
	private int hotel_no; //호텔번호
	private int book_no; //호텔예약번호
	private int booking_dognum; //애견수
	private String booking_request; //요청사항
	
	@Override
	public String toString() {
		return "BookingInfoVO [hotel_no=" + hotel_no
						+ ", book_no=" + book_no
						+ ", booking_dognum=" + booking_dognum
						+ ", booking_request=" + booking_request + "]";		

	 }

	public int getHotel_no() {
		return hotel_no;
	}

	public void setHotel_no(int hotel_no) {
		this.hotel_no = hotel_no;
	}

	public int getBook_no() {
		return book_no;
	}

	public void setBook_no(int book_no) {
		this.book_no = book_no;
	}

	public int getBooking_dognum() {
		return booking_dognum;
	}

	public void setBooking_dognum(int booking_dognum) {
		this.booking_dognum = booking_dognum;
	}

	public String getBooking_request() {
		return booking_request;
	}

	public void setBooking_request(String booking_request) {
		this.booking_request = booking_request;
	}
		
}
